def sumOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a + b

    return result

def multiple(a: float, b: float) -> float:
    result: float = None
    result = a * b

    return result

def divide(a: float, b: float) -> float:
    result: float = None
    result = a / b

    return result

def substraction(a: float, b: float) -> float:
    result: float = None
    result = a - b

    return result